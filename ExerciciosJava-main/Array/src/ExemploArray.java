
public class ExemploArray {
	public static void main(String[] args) {
		
		String nome [] = {"Manoel", "Joaquim", "Maria", "Augusto", "Silvia"};
		
		for (String n:nome) {
			System.out.println(n);
		}
		
		
		for (int i = 0; i < nome.length; i++) {
			System.out.println(nome[i]);
		}
		
	}
}

